let e = 5;
let f = e--;
console.log(`e = ${e}    f = ${f}`);

let g = 5;
let h = --g;
console.log(`g = ${g}    h = ${h}`);

let i = 5;
let j = i++;
console.log(`i = ${i}    j = ${j}`);

let k = 5;
let l = ++k;
