// 1. Pre Increment Operator (++x)
let a = 5;
console.log("a = ", ++a);

// 2. Post Increment Operator (x++)
let b = 5;
console.log("b = ", b++);

// 3. Pre Decrement Operator (--x)
let c = 5;
console.log("c = ", --c);

// 4. Post Decrement Operator (x--)
let d = 5;
console.log("d = ", d--);

