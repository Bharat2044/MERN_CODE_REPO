// calculate price based on size
// Size is 'XL', price is 250
// Size is 'L', price is 200
// Size is 'M', price is 100
// Size is 'S', price is 50

let size = 'M';

if(size === 'XL')
    console.log("Price is Rs. 250");
else if(size === 'L')
    console.log("Price is Rs. 200");
else if(size === 'M')
    console.log("Price is Rs. 100");
else        
    console.log("Price is Rs. 50");